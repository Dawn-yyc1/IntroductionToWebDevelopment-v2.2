package com.dawnc.classmodeling;

/**
 *
 * @author pisce
 */
public class Backyard {
    
    private int length;
    private int width;
    private int fencePerimeter;
    private int numTrees;

    public int getLength() {
        return length;
    }

    public void setLength(int length) {
        this.length = length;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getFencePerimeter() {
        return fencePerimeter;
    }

    public void setFencePerimeter(int fencePerimeter) {
        this.fencePerimeter = fencePerimeter;
    }

    public int getNumTrees() {
        return numTrees;
    }

    public void setNumTrees(int numTrees) {
        this.numTrees = numTrees;
    }
    
}
